package com.volkanulutas.javabasicselif.lesson.week2.lesson3_datatype;

public class W2L8_String_Special_Characters {
    public static void main(String[] args) {
        // Escape characters: ' " \
        System.out.println('\'');
        System.out.println("\"Çift tırnak işareti içerisindeki yazı\"");
        System.out.println("\\backslash içerir\\");
        // Special Characters: \n \t
        System.out.print("Hello!\tHow are you doing?\n");
        System.out.print("\"Sessizce sıkı çalışın, bırakın başarı sesiniz olsun.\"\n(Frank Ocean)");
    }
}
